package com.kodea.proyectoCalculadoraTDD;

public class Calc {
	
	public int add(int val1, int val2){
		return  val1 + val2;
	}
	
	public int substract(int val1, int val2){
		return val1 - val2;
	}
	public int multiply(int val1, int val2){
		return val1 * val2;
	}
	
	public int divide(int val1, int val2){
		return val1 / val2;
	}

}
